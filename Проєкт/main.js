const pharmacies = rawData.map(p => ({
  name: p.name,
  id: String(p.id),
  plans: {
    to: parseFloat(p.to.replace(",", ".")),
    vtm: parseFloat(p.vtm.replace(",", ".")),
    pr11: Number(p.pr11)
  }
}));

window.onload = () => {
  const dataList = document.getElementById("pharmacyList");
  pharmacies.forEach((pharmacy) => {
    const option = document.createElement("option");
    option.value = pharmacy.name;
    dataList.appendChild(option);
  });
};

function authorize() {
  const selectedName = document.getElementById("pharmacyInput").value.trim();
  const enteredID = document.getElementById("pharmacyID").value.trim();
  const pharmacy = pharmacies.find(p => p.name === selectedName);

  const errorDiv = document.getElementById("error");
  const plansDiv = document.getElementById("plans");

  if (!pharmacy || enteredID !== pharmacy.id) {
    errorDiv.innerText = "❌ Невірний ID аптеки або назва.";
    plansDiv.classList.add("hidden");
    document.getElementById("pr11Block").classList.add("hidden");
    return;
  }

  errorDiv.innerText = "";
  plansDiv.classList.remove("hidden");

  document.getElementById("planTO").innerText = `План ТО офлайн: ${pharmacy.plans.to.toLocaleString('uk-UA', {maximumFractionDigits: 2})} грн`;
  document.getElementById("planPR").innerText = `План ПР11: ${pharmacy.plans.pr11.toLocaleString('uk-UA')} грн`;
  document.getElementById("planVTM").innerText = `План ВТМ: ${pharmacy.plans.vtm.toLocaleString('uk-UA', {maximumFractionDigits: 2})} грн`;

  document.getElementById("pr11Block").classList.remove("hidden");
  document.getElementById("factPR").value = "";
  document.getElementById("pr11Result").innerText = "";
}

function calculatePR11() {
  const selectedName = document.getElementById("pharmacyInput").value.trim();
  const pharmacy = pharmacies.find(p => p.name === selectedName);

  if (!pharmacy) {
    alert("Аптека не знайдена або не авторизована.");
    return;
  }

  const planPR11 = pharmacy.plans.pr11;
  const factPR11 = parseFloat(document.getElementById("factPR").value.replace(",", "."));

  if (isNaN(factPR11)) {
    alert("Будь ласка, введіть фактичне виконання ПР11.");
    return;
  }

  const today = new Date();
  const endOfMonth = new Date(today.getFullYear(), today.getMonth() + 1, 0);
  const daysLeft = Math.max(endOfMonth.getDate() - today.getDate(), 1);

  const dailyNeed = (planPR11 - factPR11) / daysLeft;

  document.getElementById("pr11Result").innerText =
    `Необхідно виконувати по ${dailyNeed.toFixed(2)} грн/день (${daysLeft} днів залишилось)`;
}
